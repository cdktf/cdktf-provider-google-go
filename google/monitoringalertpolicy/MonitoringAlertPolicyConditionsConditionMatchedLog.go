package monitoringalertpolicy


type MonitoringAlertPolicyConditionsConditionMatchedLog struct {
	// A logs-based filter.
	//
	// Docs at Terraform Registry: {@link https://registry.terraform.io/providers/hashicorp/google/5.12.0/docs/resources/monitoring_alert_policy#filter MonitoringAlertPolicy#filter}
	Filter *string `field:"required" json:"filter" yaml:"filter"`
	// A map from a label key to an extractor expression, which is used to extract the value for this label key.
	//
	// Each entry in this map is
	// a specification for how data should be extracted from log entries that
	// match filter. Each combination of extracted values is treated as
	// a separate rule for the purposes of triggering notifications.
	// Label keys and corresponding values can be used in notifications
	// generated by this condition.
	//
	// Docs at Terraform Registry: {@link https://registry.terraform.io/providers/hashicorp/google/5.12.0/docs/resources/monitoring_alert_policy#label_extractors MonitoringAlertPolicy#label_extractors}
	LabelExtractors *map[string]*string `field:"optional" json:"labelExtractors" yaml:"labelExtractors"`
}

